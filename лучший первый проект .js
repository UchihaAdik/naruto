const $carshinobiBottom = document.querySelector('.carshinobidgrup');
const modal = document.querySelector('.modal');

  
  function generateCardHTML(arr) {
    return arr.map(item => `
      <div class="card" style="background-color:${item.color}">
          <div class="saction">
              <img class="card_img" src=${item.img} />
              <div class="sactionText">
                  <h3> name: ${item.name}</h3>
                  <h3> clan: ${item.clan}</h3>
                  <h3> gender: ${item.gender}</h3>
                  <h3> rang: ${item.rang}</h3>
                  <h3> vallage: ${item.vallage}</h3>
                  <h3> age: ${item.age}</h3>
                  <button class="btnshow" data-name="${item.name}">Узнать больше</button>
              </div>
          </div>
      </div>
    `).join('');
  }
  
  function initEventListeners() {
    const btnShowList = document.querySelectorAll('.btnshow');
  
    btnShowList.forEach(btn => {
      btn.addEventListener('click', (e) => {
        const characterName = btn.dataset.name;
        const character = shinobiWorld.find(item => item.name === characterName);
  
        fillModal(character);
  
        modal.classList.add('clickedModal');
      });
    });
  
    modal.addEventListener('click', (e) => {
      if (e.target.classList.contains('modal')) {
        modal.classList.remove('clickedModal');
      }
    });
  
    modal.addEventListener('click', (e) => {
      e.stopPropagation();
    });
  }
  
  function fillModal(character) {
    const contentModal = document.querySelector('.contentModal');
    contentModal.innerHTML = `
      <h2>${character.name}</h2>
      <img class="modalImg" src="${character.img}" alt="${character.name}" />
      <p>Clan: ${character.clan}</p>
      <p>${character.about}</p>
    `;
  }
  
  function updateCards(arr) {
    const $updatedCards = generateCardHTML(arr);
    $carshinobiBottom.innerHTML = $updatedCards;
    initEventListeners();
  }
  
  const $inputClan = document.querySelector('.inputClan');
  $inputClan.addEventListener('input', handleInputClan);
  
  const $inputName = document.querySelector('.inputName');
  $inputName.addEventListener('input', handleInputName);
  
  let displayedShinobiWorld = [...shinobiWorld];
  
  function handleInputClan(e) {
    let searchingItem = e.target.value.toLowerCase();
    displayedShinobiWorld = shinobiWorld.filter((item) => item.clan.toLowerCase().includes(searchingItem));
    updateCards(displayedShinobiWorld);
  }
  
  function handleInputName(e) {
    let searchingItem = e.target.value.toLowerCase();
    displayedShinobiWorld = shinobiWorld.filter((item) => item.name.toLowerCase().includes(searchingItem));
    updateCards(displayedShinobiWorld);
  }
  

  updateCards(shinobiWorld);







